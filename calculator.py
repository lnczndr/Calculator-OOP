# CALCULATOR USING OOP CONCEPT

class Calculator:
# pseudocode
# define add functions
    def add_input (self, inp_num1, inp_num2):
        sum = inp_num1 + inp_num2
        return sum
    
# define subtract functions
    def subtract_input (self, inp_num1, inp_num2):
        # diff = difference
        diff = inp_num1 - inp_num2
        return diff
    
# define multiplication functions
    def multiply_input (self, inp_num1, inp_num2):
        product = inp_num1 * inp_num2
        return product
    
# define division function
    def divide_input (self, inp_num1, inp_num2):
        quotient = inp_num1 / inp_num2
        return quotient
                    
